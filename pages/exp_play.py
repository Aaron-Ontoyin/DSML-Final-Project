import streamlit as st


def exp_play():
    st.write(
        """
        # Experimental Play
        This is a playground for experimenting with gesture controls.
        """
    )
    st.write(
        """
        ## Gestures
        - **Swipe Left:** Go to the next page
        - **Swipe Right:** Go to the previous page
        - **Swipe Up:** Scroll up
        - **Swipe Down:** Scroll down
        - **Pinch In:** Zoom in
        - **Pinch Out:** Zoom out
        """
    )
